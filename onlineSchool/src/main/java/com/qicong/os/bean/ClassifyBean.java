package com.qicong.os.bean;

import com.qicong.os.domain.Classify;
import com.qicong.os.domain.Course;

import java.util.ArrayList;
import java.util.List;


public class ClassifyBean extends Classify {

    //二级分类
    private List<Classify> subClassifyList = new ArrayList<Classify>();

    //推荐课程
    private List<Course> recommendCourseList;

    public List<Classify> getSubClassifyList() {
        return subClassifyList;
    }

    public void setSubClassifyList(List<Classify> subClassifyList) {
        this.subClassifyList = subClassifyList;
    }

    public List<Course> getRecommendCourseList() {
        return recommendCourseList;
    }

    public void setRecommendCourseList(List<Course> recommendCourseList) {
        this.recommendCourseList = recommendCourseList;
    }
}
