package com.qicong.os.bean;

import com.qicong.os.domain.CourseSection;

import java.util.List;


public class ChaptSectionBean {

    private Long chaptId;//章的id
    private Integer time;//时长

    private String chaptName;//章的名称
    private List<CourseSection> sections;//章里面的小节列表

    public Integer getTime() {
        return time;
    }

    public void setTime(Integer time) {
        this.time = time;
    }

    public Long getChaptId() {
        return chaptId;
    }

    public void setChaptId(Long chaptId) {
        this.chaptId = chaptId;
    }

    public String getChaptName() {
        return chaptName;
    }

    public void setChaptName(String chaptName) {
        this.chaptName = chaptName;
    }

    public List<CourseSection> getSections() {
        return sections;
    }

    public void setSections(List<CourseSection> sections) {
        this.sections = sections;
    }

}
